/**
 * @param {String} svgJsx jsx-valid SVG
 * @param {String} iconName name of icon
 * @returns {String} file content for icon react component
 */
const createIconComponent = (svgJsx, iconName) =>
  `/* Generated by fds-icons */
import React from 'react';
import SvgIcon from '@material-ui/core/SvgIcon';

const ${iconName} = (props) => (
  <SvgIcon {...props}>
    ${svgJsx}
  </SvgIcon>
);

export default ${iconName};
\n`;

module.exports = createIconComponent;
